on public.branches
for all
to authenticated
using (
  public.has_any_active_role(
    array['founder_admin', 'branch_admin']::public.app_role[]
  )
)
with check (
  public.has_any_active_role(
    array['founder_admin', 'branch_admin']::public.app_role[]
  )
);

