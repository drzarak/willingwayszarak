create or replace function public.has_any_active_role(target_roles public.app_role[])
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_roles ur
    where ur.user_id = auth.uid()
      and ur.status = 'active'
      and ur.role = any(target_roles)
  );
$$;

create or replace function public.has_active_role(
  target_role public.app_role,
  target_branch uuid default null
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_roles ur
    where ur.user_id = auth.uid()
      and ur.status = 'active'
      and ur.role = target_role
      and (
        target_branch is null
        or ur.branch_id is null
        or ur.branch_id = target_branch
      )
  );
$$;

create or replace function public.is_care_staff()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.has_any_active_role(
    array[
      'founder_admin',
      'branch_admin',
      'doctor',
      'counselor',
      'staff'
    ]::public.app_role[]
  );
$$;

create or replace function public.handle_new_auth_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  cleaned_username text;
  resolved_language public.profile_language;
begin
  cleaned_username := nullif(
    lower(
      regexp_replace(
        coalesce(new.raw_user_meta_data ->> 'username', ''),
        '[^a-z0-9_]+',
        '',
        'g'
      )
    ),
    ''
  );

  resolved_language := case lower(coalesce(new.raw_user_meta_data ->> 'preferred_language', ''))
    when 'urdu' then 'urdu'
    when 'punjabi' then 'punjabi'
    when 'no_preference' then 'no_preference'
    else 'english'
  end;

  insert into public.profiles (
    id,
    username,
    full_name,
    phone_e164,
    preferred_language
  )
  values (
    new.id,
    cleaned_username,
    nullif(trim(coalesce(new.raw_user_meta_data ->> 'full_name', '')), ''),
    nullif(trim(coalesce(new.phone, '')), ''),
    resolved_language
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

do $$
begin
  if not exists (
    select 1
    from pg_trigger
    where tgname = 'on_auth_user_created_profile'
  ) then
    create trigger on_auth_user_created_profile
    after insert on auth.users
    for each row
    execute function public.handle_new_auth_user_profile();
  end if;
end;
$$;

