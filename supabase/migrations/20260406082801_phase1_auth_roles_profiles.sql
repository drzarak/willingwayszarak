-- Phase 1 foundation schema for:
-- - Auth-linked profiles and roles
-- - Branch structure
-- - Lightweight patient/family linkage
-- - RLS-friendly helper functions and policies

create extension if not exists pgcrypto with schema extensions;

do $$
begin
  if not exists (
    select 1
    from pg_type
    where typname = 'app_role'
      and typnamespace = 'public'::regnamespace
  ) then
    create type public.app_role as enum (
      'founder_admin',
      'branch_admin',
      'doctor',
      'counselor',
      'staff',
      'patient',
      'family_member',
      'pending_clinician',
      'pending_staff'
    );
  end if;
end;
$$;

do $$
begin
  if not exists (
    select 1
    from pg_type
    where typname = 'membership_status'
      and typnamespace = 'public'::regnamespace
  ) then
    create type public.membership_status as enum (
      'pending',
      'active',
      'suspended'
    );
  end if;
end;
$$;

do $$
begin
  if not exists (
    select 1
    from pg_type
    where typname = 'profile_language'
      and typnamespace = 'public'::regnamespace
  ) then
    create type public.profile_language as enum (
      'english',
      'urdu',
      'punjabi',
      'no_preference'
    );
  end if;
end;
$$;

create table if not exists public.branches (
  id uuid primary key default gen_random_uuid(),
  code text not null unique,
  name text not null,
  city text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id uuid primary key references auth.users (id) on delete cascade,
  username text unique,
  full_name text,
  phone_e164 text,
  preferred_language public.profile_language not null default 'english',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_username_format check (
    username is null or username ~ '^[a-z0-9_]{3,32}$'
  )
);

create table if not exists public.user_roles (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users (id) on delete cascade,
  role public.app_role not null,
  branch_id uuid references public.branches (id) on delete set null,
  status public.membership_status not null default 'active',
  assigned_by uuid references auth.users (id) on delete set null,
  created_at timestamptz not null default now()
);

create unique index if not exists user_roles_user_role_branch_uniq
  on public.user_roles (
    user_id,
    role,
    coalesce(branch_id, '00000000-0000-0000-0000-000000000000'::uuid)
  );

create table if not exists public.patients (
  id uuid primary key default gen_random_uuid(),
  auth_user_id uuid unique references auth.users (id) on delete set null,
  external_code text,
  primary_branch_id uuid references public.branches (id) on delete set null,
  created_by uuid references auth.users (id) on delete set null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.family_members (
  id uuid primary key default gen_random_uuid(),
  auth_user_id uuid unique references auth.users (id) on delete set null,
  relation_label text,
  created_by uuid references auth.users (id) on delete set null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.patient_family_links (
  id uuid primary key default gen_random_uuid(),
  patient_id uuid not null references public.patients (id) on delete cascade,
  family_member_id uuid not null references public.family_members (id) on delete cascade,
  relationship_to_patient text,
  can_view_patient_profile boolean not null default true,
  can_view_tasks boolean not null default true,
  is_primary_contact boolean not null default false,
  created_by uuid references auth.users (id) on delete set null,
  created_at timestamptz not null default now(),
  unique (patient_id, family_member_id)
);

create index if not exists idx_user_roles_user_status
  on public.user_roles (user_id, status);

create index if not exists idx_user_roles_role_branch
  on public.user_roles (role, branch_id, status);

create index if not exists idx_patients_auth_user
  on public.patients (auth_user_id);

create index if not exists idx_family_members_auth_user
  on public.family_members (auth_user_id);

create index if not exists idx_patient_family_links_patient
  on public.patient_family_links (patient_id);

create index if not exists idx_patient_family_links_family
  on public.patient_family_links (family_member_id);

