drop policy if exists patients_select_policy on public.patients;
create policy patients_select_policy
on public.patients
for select
to authenticated
using (
  public.is_care_staff()
  or auth_user_id = auth.uid()
  or exists (
    select 1
    from public.patient_family_links pfl
    join public.family_members fm
      on fm.id = pfl.family_member_id
    where pfl.patient_id = patients.id
      and fm.auth_user_id = auth.uid()
      and pfl.can_view_patient_profile
  )
);

